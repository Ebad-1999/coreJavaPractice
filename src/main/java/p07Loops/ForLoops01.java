package p07Loops;

public class ForLoops01 {
    public static void main(String[] args) {

        String name  = "Mark";
        String reName = "";
        for (int i = name.length()-1; i>=0 ; i--){
            char ch = name.charAt(i);
            reName=reName+ch;
            System.out.print(ch);
        }


















    }
}
