package p07Loops;

public class NestedForLoops01 {
    public static void main(String[] args) {

        for (int i=1; i<5; i++){
            System.out.println("Week:"+i);

            for (int k = 1;  k<=7 ;k++){
                System.out.println("Day: " + k);
            }
        }













    }
}
